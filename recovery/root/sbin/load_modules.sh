#!/system/bin/sh

touch_class_path=/sys/class/touchscreen
touch_path=/sys/devices/platform/soc/99000.spi/spi_master/spi1/spi1.0
firmware_path=/vendor/firmware
firmware_file=/vendor/firmware/novatek_nt36675_j3s_fw01.bin
device=$(getprop ro.boot.device)

wait_for_poweron()
{
	local wait_nomore
	local readiness
	local count
	wait_nomore=60
	count=0
	while true; do
		readiness=$(cat $touch_path/poweron)
		if [ "$readiness" == "1" ]; then
			break;
		fi
		count=$((count+1))
		[ $count -eq $wait_nomore ] && break
		sleep 1
	done
	if [ $count -eq $wait_nomore ]; then
		return 1
	fi
	return 0
}

wait_for_poweron
echo $firmware_file > $touch_path/doreflash
echo 1 > $touch_path/forcereflash
sleep 5
echo 1 > $touch_path/reset

return 0
